package br.com.utilities.exceptions;

public class PathIteratorException extends Exception {


	/**
	 *
	 */
	private static final long serialVersionUID = 3228518253710898327L;
	
	public PathIteratorException(String message) {
		super(message);
	}
	
	public PathIteratorException(Throwable cause) {
		super(cause);
	}
	
	public PathIteratorException(String message, Throwable cause) {
		super(message, cause);
	}

}
