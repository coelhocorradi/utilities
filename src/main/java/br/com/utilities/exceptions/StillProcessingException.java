package br.com.utilities.exceptions;

public class StillProcessingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8648039181434918362L;

	public StillProcessingException() {
		super();
	}

	public StillProcessingException(String arg0) {
		super(arg0);
	}

	public StillProcessingException(Throwable arg0) {
		super(arg0);
	}

	public StillProcessingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
