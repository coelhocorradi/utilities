package br.com.utilities.constants;

public abstract class Times {

	public static final int immediately = 0;

	/**
	 * 1 segundo em milisegundos
	 */
	public static final int _1s = 1000;

	/**
	 * 2 segundos em milisedundos
	 */
	public static final int _2s = 2 * _1s;

	/**
	 * 5 segundos em milisegundos
	 */
	public static final int _5s = 5 * _1s;

	/**
	 * 30 segundos em milisegundos
	 */
	public static final int _30s = _1s * 30;

	/**
	 * 1 minuto em milisegundos
	 */
	public static final int _1M = 60 * _1s;

	/**
	 * 2 minutos em milisegundos
	 */
	public static final int _2M = 2 * _1M;

	/**
	 * 5 minutos em milisegundos
	 */
	public static final int _5M = 5 * _1M;

	/**
	 * 10 minutos em milisegundos
	 */
	public static final int _10M = 10 * _1M;

	/**
	 * 30 minutos em milisegundos
	 */
	public static final int _30M = 30 * _1M;

	/**
	 * 1 hora em milisegundos
	 */
	public static final int _1H = 60 * _1M;

	/**
	 * 2 horas em milisegundos
	 */
	public static final int _2H = _1H * 2;

	/**
	 * 6 horas em milisegundos
	 */
	public static final int _6H = _1H * 6;

	/**
	 * 12 horas em milisegundos
	 */
	public static final int _12H = _1H * 12;

	/**
	 * 24 horas em milisegundos
	 */
	public static final int _1D = _1H * 24;
}